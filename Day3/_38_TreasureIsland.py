    swim_wait = input("Do you want to Swim or Wait? S or W")
    if swim_wait == "S":
        print("Attacked by gator. Game Over !!")
    else:
        door = input("Which door to choose. Red, Blue or Yellow?")
        if door == "Red":
            print("Burned by fire. Game Over !!")
        elif door == "Blue":
            print("Eaten by beasts. Game Over !!")
        elif door == "Yellow":
            print("You Win !!")
        else:
            print("Game Over!! Your selection didn't work.")

